package consul

import (
	"fmt"
	"testing"

	"github.com/stretchr/testify/assert"
)

func TestLookupServiceHost(t *testing.T) {
	rslv, err := NewResolver("CounterService", "")
	assert.Nil(t, err)

	host, _, err := rslv.LookupServiceHost("CounterService", "DEV")
	assert.Nil(t, err)
	fmt.Println("HOST: ", host)
}
